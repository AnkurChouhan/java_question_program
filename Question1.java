public class Question1 {
    public static void main(String[] args) {
        // Variable declarations
        int intNum1 = 10;
        int intNum2 = 10;
        double doubleNum1 = 31.0;
        double doubleNum2 = 2.0;
        char character = 'A';
        boolean isTrue = true;
        String greeting = "Hello, ";
        String subject = "world!";

        // Arithmetic operations
        int intSum = intNum1 + intNum2;
        double doubleDivision = doubleNum1 / doubleNum2;
        String fullGreeting = greeting + subject;

        // Display results
        System.out.println("Integer result of addition: " + intSum);
        System.out.println("Double result of division: " + doubleDivision);
        System.out.println("Character: " + character);
        System.out.println("Boolean value: " + isTrue);
        System.out.println("String concatenation result: " + fullGreeting);
    }
}
